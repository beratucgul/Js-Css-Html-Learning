console.log(1 == 1)     /* return true */


console.log(1 === 1)    /* return true */


console.log(1 == "1")   /* return true */


console.log(1 === "1")  /* return false */


console.log(0 == false)  /* return true */


console.log(0 === false)  /* return false */


console.log(null === undefined) /* return true */


console.log(null === undefined) /* return false */


console.log(1 != "1")   /* return false */


console.log(1 !== "1")  /* return true */